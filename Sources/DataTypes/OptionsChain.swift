public struct OptionsContract
{
	public var contractSymbol: String?
	public var strike: Float?
	public var currency: String?
	public var lastPrice: Float?
	public var change: Float?
	public var percentChange: Float?
	public var volume: Int?
	public var openInterest: Int?
	public var bid: Float?
	public var ask: Float?
	public var contractSize: String?
	public var expiration: Date?
	public var lastTradeDate: Date?
	public var impliedVolatility: Float?
	public var inTheMoney: Bool?
	
	init(information: JSON) {
		
		let expiration_op:Date?
		if let expiration = information["expiration"].int
		{
			expiration_op = Date(timeIntervalSince1970: Double(expiration))
		}
		else
		{
			expiration_op = nil
		}
		let last_op:Date?
		if let last = information["lastTradeDate"].int
		{
			last_op = Date(timeIntervalSince1970: Double(last))
		}
		else
		{
			last_op = nil
		}
		self.expiration = makeDateFromIntTimeIntervalOrNil(information:information["expiration"])
		self.lastTradeDate = makeDateFromIntTimeIntervalOrNil(information:information["lastTradeDate"])
		self.contractSymbol = information["contractSymbol"].string
		self.strike = information["strike"].float
		self.currency = information["currency"].string
		self.lastPrice = information["lastPrice"].float
		self.change = information["change"].float
		self.percentChange = information["percentChange"].float
		self.volume = information["volume"].int
		self.openInterest = information["openInterest"].int
		self.bid = information["bid"].float
		self.ask = information["ask"].float
		self.contractSize = information["contractSize"].string
		self.expiration = expiration_op
		self.lastTradeDate = last_op
		self.impliedVolatility = information["impliedVolatility"].float
		self.inTheMoney = information["contractSymbol"].bool
	}
}

public struct OptionsContractsByExpiry
{
	public var expirationDate: Date?
	public var hasMiniOptions: Bool?
	public var calls: [OptionsContract]
	public var puts: [OptionsContract]
	
	init(information: JSON) {
		
		self.expirationDate = makeDateFromIntTimeIntervalOrNil(information:information["expirationDate"])
		self.hasMiniOptions = information["hasMiniOptions"].bool

		var call_arr: [OptionsContract] = []
		for val in information["calls"].array! {
			let contract = OptionsContract(information: val)
			call_arr.append(contract)
		}
		self.calls = call_arr

		var put_arr: [OptionsContract] = []
		for val in information["puts"].array! {
			let contract = OptionsContract(information: val)
			put_arr.append(contract)
		}
		self.puts = put_arr

	}
	
	
}


public struct OptionsChain {
	public var underlyingSymbol: String?
	public var expirationDates: [Date?]
	public var strikes: [Float?]
	public var hasMiniOptions: Bool?

	public var quote: Quote?
	public var options: [OptionsContractsByExpiry]
	
	public var dataStorage: JSON?

	init(information: JSON) {
		self.dataStorage = information
		
		self.underlyingSymbol = information["underlyingSymbol"].string

		var exp_dates: [Date?] = []
		for exp in information["expirationDates"].array! {
			let date = makeDateFromIntTimeIntervalOrNil(information:exp)
			exp_dates.append(date)
		}
		self.expirationDates = exp_dates
		
		var stike_arr: [Float?] = []
		for val in information["strikes"].array! {
			let strike = val.float
			stike_arr.append(strike)
		}
		self.strikes = stike_arr

		self.hasMiniOptions = information["hasMiniOptions"].bool

		
		if information["quote"].dictionary != nil {
			self.quote = Quote(information: information["quote"])
		}
		
		
		var options_contracts_by_expiry: [OptionsContractsByExpiry] = []
		if let options_arr = information["options"].array
		{
			for val in options_arr {
				let contract = OptionsContractsByExpiry(information: val)
				options_contracts_by_expiry.append(contract)
			}
		}
		self.options = options_contracts_by_expiry
		
		
	}
}


